public class Rotation {
    private double x;
    private double y;
    private double z;
    private double w;
    public double getX() {
        return x;
    }
    public void setX(double x) {
        this.x = x;
    }
    public double getY() {
        return y;
    }
    public void setY(double y) {
        this.y = y;
    }
    public double getZ() {
        return z;
    }
    public void setZ(double z) {
        this.z = z;
    }
    public double getW() {
        return w;
    }
    public void setW(double w) {
        this.w = w;
    }
}